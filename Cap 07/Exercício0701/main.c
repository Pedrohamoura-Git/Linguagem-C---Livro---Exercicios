//-------------- PROTÓTIPOS DAS FUNÇÕES ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------


int strlen(char *s);
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
int main()
{
    printf("Hello world!\n\n");

//------ Definindo elementos
    char s[LIM];

//------ Armazenando o input do usuário
    printf("Insira um nome: ");
    gets(s);

//------ Imprimindo o resultado
    printf("\nTamanho do nome inserido: %d\n", strlen(s));

//------ .The End...?
    return 0;
}
//-------------- FUNÇÕES -------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

int strlen(char *s)
{
//------ Definindo elementos
    int i, cont;

//------ Contando caracteres
    for(i = 0, cont = 0; i < LIM; i++)
    {
        if(s[i] != '\0')
        {
            cont++;
        }
        else
        {
            return cont;
        }

    }
}

